/** Resolve a key for a language, falling back to English, then the key itself.
 *  Supports {placeholder} interpolation via the `vars` map. */
export function translate(lang, key, vars) {
  let str = dict[lang]?.[key] ?? dict.en[key] ?? key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, "g"), v);
    });
  }
  return str;
}

/** Client-side hook — reads the active language from Redux (which is now
 *  correctly persisted per Fix 44's siteSettingsSlice patch) and returns a
 *  `t(key, vars)` translator function bound to it. */
export function useTranslation() {
  const lang = useSelector((s) => s.siteSettings?.language?.activeLanguage) || "en";
  const t = (key, vars) => translate(lang, key, vars);
  return { t, lang };
}
